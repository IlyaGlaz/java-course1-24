package lesson1;

public class Primitives {

    public static void main(String[] args) {
        System.out.println(22); // int

        System.out.println(22L); // long

        System.out.println(0.22); // double

        System.out.println(0.22F); // float

        System.out.println('B'); // char

        System.out.println(false); // boolean

        System.out.println("Hello"); // String
    }
}
